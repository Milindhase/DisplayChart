package stringdisplay;

public class stringdisplay {
	
	 public static void main(String[] args) {
	
	int i,j,n=10;
	System.out.println("Display Triangle");
	
	for (i=1;i<=n;i++)
	{
		
		for (j=1;j<=i;j++)
		{	
			System.out.print(" ");
			System.out.print("*");	
			System.out.print(" ");
		 
}
		System.out.println(" ");
}
	 
}
}
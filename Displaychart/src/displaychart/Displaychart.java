package displaychart;

public class Displaychart {
	
	 public static void main(String[] args) {
	
	int i,j,n=10;
	System.out.println("Display Multiplication Chart");
	
	for (i=1;i<=n;i++)
	{
		
		for (j=1;j<=n;j++)
		{	
			
		
		System.out.print  (  i  *  j );
		System.out.print("  "); 
 }
		System.out.println("  ");
		System.out.println("  ");
		
 }
}
}